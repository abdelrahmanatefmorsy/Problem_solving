# define ll long long

// *********************************************************************
/**
 * Integer shift left
 */
int shift_left(int x, int i){
    return x << i;
}
/**
 * long long  shift left
 */
ll shift_left(ll x, ll i){
    return x << i;
}
/**
 * int shift right
 */
int shift_right(int x, int i){
    return x >> i;
}
/**
 * ll shift right
 */
ll shift_right(ll x, ll i){
    return x >> i;
}
/**
 * int shift right
 */
int shift_right(int x, int i){
    return x >> i;
}
